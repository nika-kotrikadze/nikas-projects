week_day=int(input("enter number 1 to 6: "))

if week_day==1:
    print("monday")

elif week_day==2:
    print("tuesday")

elif week_day==3:
    print("wednesday")

elif week_day==4:
    print("thursday")

elif week_day==5:
    print("saturday")

elif week_day==6:
    print("sunday")

else:
    print("error")